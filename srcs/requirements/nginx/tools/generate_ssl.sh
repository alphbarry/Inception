#!/bin/sh
set -e

CERT_DIR=/etc/nginx/ssl
mkdir -p $CERT_DIR

if [ ! -f "$CERT_DIR/server.crt" ]; then
  openssl req -x509 -nodes -days 365 \
    -newkey rsa:2048 \
    -keyout $CERT_DIR/server.key \
    -out $CERT_DIR/server.crt \
    -subj "/C=FR/ST=IDF/L=Paris/O=42/CN=$DOMAIN_NAME"
fi

exec nginx -g "daemon off;"

